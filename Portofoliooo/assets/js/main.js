// Get the modal elements
const modal = document.getElementById("certificationModal");
const modalImg = document.getElementById("modalImage");
const captionText = document.getElementById("caption");

// Get all certification cards and set up click events
const certificationCards = document.querySelectorAll(".certification-card");
certificationCards.forEach(card => {
    card.onclick = function () {
        modal.style.display = "block";

        // Use the icon within the card as the modal image source
        const iconImage = card.querySelector(".certification-logo");
        modalImg.src = iconImage.src;
        captionText.innerHTML = iconImage.alt;
    };
});

// Close modal on click of close button
const closeBtn = document.getElementsByClassName("close")[0];
closeBtn.onclick = function () {
    modal.style.display = "none";
};
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();

        document.querySelector(this.getAttribute('href')).scrollIntoView({
            behavior: 'smooth'
        });
    });
});